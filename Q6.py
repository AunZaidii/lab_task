# A car is running at a velocity of 50 miles per hour and the driver accelerates the car by 10
# miles/hr2. How far the car travels from this point in the next 2 hours, if the acceleration is
# constant.

u = 50
a = 10
t = 2
s = (u*t)+(1/2)*(a)*(t**2)
print("The car will travel a distance of",s,"miles in the next 2 hours")
