# Q1. A ball at the end of a string is revolving uniformly in a horizontal circle of radius 2 meters at constant
# angular speed 10 rad/s. Determine the magnitude of the linear velocity of a point located:
# # (a) 0.5 meters from the center
# (b) 1 meter from the center
# (c) 2 meters from the center

r1 = 0.5
r2 = 1
r3 = 2
